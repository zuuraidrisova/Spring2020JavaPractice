package day18_NestedLoops;

public class do_while_Loops {

    public static void main(String[] args) {

        int num=103;//print numbers between 100~num

        while(num>=100){

            System.out.println(num);
            num--;
        }

        System.out.println("==============");


        int num2 =103;

        do{

            System.out.println(num2);
            num2--;

        }while (num2>=103);


    }
}
