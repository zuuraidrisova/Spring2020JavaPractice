package day29_Wrapper_ArrayList;

public class Max_Value_Min_Value {

    public static void main(String[] args) {

        int maxNum = Integer.MAX_VALUE; //return max value of int and return it

        System.out.println(maxNum);

        double maxNum2 = Double.MAX_VALUE; //returns max value of double

        System.out.println(maxNum2);

        short maxNum3 = Short.MAX_VALUE;//returns max value of short

        System.out.println(maxNum3);

        System.out.println("=======================");

        int minNum = Integer.MIN_VALUE;//returns min value of int

        System.out.println(minNum);

        byte minNum2 = Byte.MIN_VALUE;//returns min value of byte

        System.out.println(minNum2);

        float minNum3 = Float.MIN_VALUE;//returns min value of float

        System.out.println(minNum3);

    }
}
