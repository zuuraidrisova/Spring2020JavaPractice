package OfficeHours.Practice_03_11_2020;

public class Practice_EscapeSequences {

    public static void main(String [] args){
        System.out.println("\t Java is a programming language");
        // single line comment starts with double slash and for short comments
        System.out.println("\t Java is a very popular programming language");
        System.out.println("\n\tWe love learning Java \n\n\t  and it is very fun.");
        // we can use \n and \t together in one line
        System.out.println("\tI love the TV show \"The Ellen Show\". ");
        System.out.println("My favorite quote is from the movie \"ForrestGump\",which sounds like \"Life is like a box of chocolates, you never know which you are gonna get\".");
        // \" used for double quotes
        System.out.println("\\");
        // \\ is used to print single back slash

    }

}
